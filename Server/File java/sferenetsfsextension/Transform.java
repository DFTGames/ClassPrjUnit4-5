package sferenetsfsextension;

/**
 *
 * @author Ninfea
 */
public class Transform {
    float x;
    float y;
    float z;
    float rx;
    float ry;
    float rz;
 
    public Transform(float x, float y, float z, float rx, float ry, float rz) {
		this.x = x;
		this.y = y;
		this.z = z;

		this.rx = rx;
		this.ry = ry;
		this.rz = rz;
                
		
	}
}
